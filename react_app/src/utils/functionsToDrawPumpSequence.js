  export const drawPumpSequence = (fluid, color) => {
    const shapesAllPumpSequence = [];
    const allX0Y0 = [];
    const allX1Y1 = [];
    const allX0 = [];
    const allX1 = [];
    const allY0 = [];
    const allY1 = [];
  
      /* For to create all x0 , y0 */
    for (let index = 0; index < fluid.length; index += 1) {
      if (index % 2 === 0) {
        const testeX0Y0 = fluid[index][2];
        allX0Y0.push(...testeX0Y0);
      } else {
        const testeX1Y1 = fluid[index][3];
        allX1Y1.push(...testeX1Y1);
      }
    }
  
     /* For to create all x1 , y1 */
  
    for (let index = 0; index < allX0Y0.length; index += 1) {
      if (index % 2 === 0) {
        const X0 = allX0Y0[index];
        const X1 = allX1Y1[index];
        allX0.push(X0);
        allX1.push(X1);
      } else {
        const Y0 = allX0Y0[index];
        const Y1 = allX1Y1[index];
        allY0.push(Y0);
        allY1.push(Y1);
      }
    }
  
      /* For to create all shapes for pump sequence fluids */
  
    for (let index = 0; index < allX0.length; index += 1) {
      const internalShape = {
        type: "rect",
  
        x0: allX0[index],
  
        y0: allY0[index],
  
        x1: allX1[index],
  
        y1: allY1[index],
  
        line: {
          color: color,
          width: 2,
        },
        opacity: 0.8,
        fillcolor: color,
      };
      shapesAllPumpSequence.push(internalShape);
    }
    return shapesAllPumpSequence;
  };
  
